def query_vision(prompt, images):
    API_KEY = os.environ.get("OPENAI_API_KEY", "")
    headers = {"Authorization": f"Bearer {API_KEY}", "Content-Type": "application/json"}
    content_parts = [{"type": "text", "text": "You are looking at real images attached to this message. You CAN see them. Analyze them thoroughly.\n\n" + prompt}]
    for img in images:
        try:
            pil_img = None
            if isinstance(img, Image.Image):
                pil_img = img
            elif isinstance(img, str) and os.path.isfile(img):
                pil_img = Image.open(img)
            elif hasattr(img, "name") and os.path.isfile(getattr(img, "name", "")):
                pil_img = Image.open(img.name)
            elif isinstance(img, dict) and "path" in img:
                pil_img = Image.open(img["path"])
            else:
                continue
            if pil_img.mode in ("RGBA", "P", "LA"):
                pil_img = pil_img.convert("RGB")
            if max(pil_img.size) > 1024:
                pil_img.thumbnail((1024, 1024))
            buf = io.BytesIO()
            pil_img.save(buf, format="JPEG", quality=85)
            b64 = base64.b64encode(buf.getvalue()).decode("utf-8")
            content_parts.append({
                "type": "image_url",
                "image_url": {"url": "data:image/jpeg;base64," + b64}
            })
        except Exception:
            continue
    msgs = [{"role": "user", "content": content_parts}]
    payload = {"model": "gpt-4o", "max_tokens": 4000, "messages": msgs}
    try:
        response = requests.post("https://api.openai.com/v1/chat/completions", headers=headers, json=payload, timeout=240)
        result = response.json()
        if "choices" in result:
            return result["choices"][0]["message"]["content"]
        return "API Error: " + str(result)
    except Exception as e:
        return "Error: " + str(e)

def extract_pdf_text(pdf_file):
    if pdf_file is None:
        return ""
    try:
        if pdf_file.name.lower().endswith(('.jpg', '.jpeg', '.png', '.webp')):
            return f"[Image document uploaded: {pdf_file.name}]"
        reader = PdfReader(pdf_file.name)
        text = ""
        for page in reader.pages:
            extracted = page.extract_text()
            if extracted:
                text += extracted + "\n"
        return text.strip()
    except Exception as e:
        return f"Error reading file: {str(e)}"

