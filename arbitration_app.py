def chat_with_documents(
    message,
    doc1=None, doc2=None, doc3=None, doc4=None,
    chat_context="",
    history=None
):
    if history is None:
        history = []
    if not message:
        return history, ""

    doc_texts = []
    doc_labels = ["Document 1", "Document 2", "Document 3", "Document 4"]
    docs = [doc1, doc2, doc3, doc4]

    for i, doc in enumerate(docs):
        if doc is not None:
            text = extract_pdf_text(doc)
            if text:
                doc_texts.append(f"=== {doc_labels[i]} ({doc.name}) ===\n{text[:2000]}")

    img_docs = []
    for doc in docs:
        if doc is not None and doc.name.lower().endswith(('.jpg', '.jpeg', '.png', '.webp')):
            try:
                img = Image.open(doc.name)
                img_docs.append(img)
            except:
                pass

    combined_docs = "\n\n".join(doc_texts) if doc_texts else ""
    context_info = "Additional context: " + chat_context if chat_context.strip() else ""

    conv_history = ""
    if history:
        for h in history[-3:]:
            conv_history += f"User: {h[0]}\nAssistant: {h[1]}\n\n"

    if img_docs and not doc_texts:
        vision_prompt = (
            "You are an expert insurance professional AI assistant. Write as the professional analyst who performed this work. State all findings directly with specific numbers, percentages, and comparisons. NEVER say 'this appears to be', 'the data seems to show', 'the dataset contains', or 'it looks like'. Instead, deliver results confidently: 'Average claim cost was $4,230, a 12% increase year-over-year' or 'Readmission rates of 18.3% in Cardiology exceeded the facility average by 4.1 percentage points.' Be direct, precise, and actionable. Present findings as YOUR analysis, not a description of a dataset. "
            "Previous conversation:\n" + conv_history +
            "\nContext: " + context_info +
            "\n\nUser question: " + message +
            "\n\nProvide a detailed, professional insurance analysis response."
        )
        response = query_vision(vision_prompt, img_docs)
    elif combined_docs:
        prompt = (
            "You are an expert insurance professional AI assistant specializing in "
            "claims analysis, arbitration, total loss, bodily injury, and liability.\n\n"
            "UPLOADED DOCUMENTS:\n" + combined_docs + "\n\n"
            + context_info + "\n\n"
            "CONVERSATION HISTORY:\n" + conv_history +
            "User: " + message + "\n\n"
            "Provide a detailed, professional response based on the uploaded documents."
        )
        response = query_llm(prompt)
    else:
        prompt = (
            "You are an expert insurance professional AI assistant specializing in "
            "claims analysis, arbitration, total loss, bodily injury, and liability.\n\n"
            + context_info + "\n\n"
            "CONVERSATION HISTORY:\n" + conv_history +
            "User: " + message + "\n\n"
            "Provide a detailed, professional insurance response."
        )
        response = query_llm(prompt)

    response += "\n\n" + WATERMARK
    history.append({"role": "user", "content": message})
    history.append({"role": "assistant", "content": response})
    return history, ""

